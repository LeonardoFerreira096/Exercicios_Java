public class ExeArrays {
    public static void main(String[] args){
        String [] nomes = new String[3];

        nomes[0] = "Maria";
        nomes[1] = "Leonardo";
        nomes[2] = "Inessa";

        for (int i = 0; i < nomes.length; i++) {
            System.out.println(nomes[i]);
        }

    }
}
